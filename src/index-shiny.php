        <div class="text">
          <p>The results of this 2007-10 project show that the oldest papers are often in the best condition, in part, we believe, because they contain high levels of gelatin and calcium.  &ldquo;People often wonder why in the digital age we should be concerned about paper stability,&rdquo; says principal investigator Timothy Barrett. &ldquo;Artifacts on paper often contain valuable information related to human history that is not accessible in a digital image. Not only do these artifacts need to be preserved for future generations, but paper copies that can be read without  electronic hardware will continue to be essential backups to the digital record long into the future.&rdquo; The results of this research will be of special interest to paper historians, paper and book conservators, and producers of archival papers. (Click on <strong>RESULTS Plot Library</strong>, for a full description of the plot above.)</p>
          <p>This website is designed for use by a wide range of visitors, from the complete novice to the paper-conservation scientist. Newcomers to the site may want to begin with the<strong> PROJECT OVERVIEW &amp; AUTHORS</strong> and  <strong>CONCLUSIONS</strong> sections for a quick sense of our research and what we learned. Those unfamiliar with papermaking history and technique may wish to start with <strong>European Papermaking Techniques 1300-1800</strong> (under <strong>BACKGROUND</strong>) for an introduction to the craft. Visitors  with a strong interest in papermaking history, materials and processes, paper permanence, paper science, and paper conservation are advised to begin at the top of the menu to the left and click on each tab, reading as interest and time permit.  The site will be updated regularly. Suggestions for changes are welcome via email messages to the principal investigator, Timothy Barrett  (<a href="mailto:timothy-barrett@uiowa.edu">timothy-barrett@uiowa.edu</a>). Click <a href="http://dx.doi.org/10.1515/res-2015-0017">here</a> to access a peer reviewed 2016 article summarizing this research. <br>
