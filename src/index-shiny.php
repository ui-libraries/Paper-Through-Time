              <p>Historical papers vary considerably in their present-day condition for reasons that are not fully understood. This website shares the results of research on 1,578 paper specimens made in Europe and America between the fourteenth and the nineteenth centuries. The papers tested were selected from collections at the Newberry Library and The University of Iowa, and were analyzed using nondestructive instrumentation. The Institute of Museum and Library Services, the Kress Foundation, and The University of Iowa provided funding support.</p>
